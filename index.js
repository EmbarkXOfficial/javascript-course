// EXPRESSION
// An expression in JavaScript is any valid 
// unit of code that produces a value


// LITERAL
// A literal is a fixed value that you write 
// directly in the code.


// OPERATORS
// Operators in JavaScript are symbols that 
// perform operations on operands.






/*
Types of Operators in JavaScript:

1. Arithmetic Operators:
   - Addition (+)
   - Subtraction (-)
   - Multiplication (*)
   - Division (/)
   - Modulus (%)
   - Increment (++)
   - Decrement (--)

2. Assignment Operators:
   - Assignment (=)
   - Addition assignment (+=)
   - Subtraction assignment (-=)
   - Multiplication assignment (*=)
   - Division assignment (/=)

3. Comparison Operators:
   - Equality (==)
   - Strict equality (===)
   - Inequality (!=)
   - Strict inequality (!==)
   - Greater than (>)
   - Less than (<)
   - Greater than or equal to (>=)
   - Less than or equal to (<=)

4. Logical Operators:
   - Logical AND (&&)
   - Logical OR (||)
   - Logical NOT (!)

5. Unary Operators:
   - Increment (++)
   - Decrement (--)
   - typeof operator
   - Logical NOT (!)

6. Ternary Operator:
   - Conditional operator (condition ? value1 : value2)
*/
