/*
Comparison Operators:
   - Equality (==)
   - Strict equality (===)
   - Inequality (!=)
   - Strict inequality (!==)
   - Greater than (>)
   - Less than (<)
   - Greater than or equal to (>=)
   - Less than or equal to (<=)
*/

// Equality (==)
let a = 5;
let b = 5;
console.log(5 == 6);
console.log(a == b);
console.log(typeof '6');
console.log(5 == '5');

// Strict equality (===)
console.log('5' === '6');


// Inequality (!=)
console.log(3 != 3);
console.log(3 != '3');

// Strict inequality (!==)
console.log(3 !== '3');
console.log('5' !== '5');


// - Greater than (>)
// - Less than (<)
console.log(5 > 3);
console.log(5 > 7);

console.log(5 < 3);
console.log(5 < 7);


// - Greater than or equal to (>=)
// - Less than or equal to (<=)
console.log(5 <= 5);
console.log(5 >= 5);












